    private static void GenerateConfigureFromAttributes(StringBuilder sb, ImmutableArray<MessageInfo> messages)
    {
        // Filter messages that have at least one known attribute
        // For non-notification messages, a response type is required; notifications may have WithOutbox.
        var attributedMessages = messages.Where(m =>
            (m.HasTransactional || m.HasAuditable || m.HasRequiresTenant || m.HasAuthorize || m.HasWithOutbox) &&
            (m.ResponseTypeFullName != null || m.HasWithOutbox)
        ).ToList();

        if (attributedMessages.Count == 0) return;

        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Configures pipeline behaviors based on attributes detected at compile time.");
        sb.AppendLine("        /// This is a zero-reflection replacement for *FromAttributes methods.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        /// <remarks>");
        sb.AppendLine("        /// Detected attributes: [Transactional], [Auditable], [RequiresTenant], [Authorize], [WithOutbox].");
        sb.AppendLine("        /// Usage: services.AddTurboMediator(builder =&gt; builder.ConfigureFromAttributes());");
        sb.AppendLine("        /// </remarks>");
        sb.AppendLine("        public static TurboMediatorBuilder ConfigureFromAttributes(this TurboMediatorBuilder builder)");
        sb.AppendLine("        {");

        foreach (var msg in attributedMessages)
        {
            var responseType = msg.ResponseTypeFullName ?? "TurboMediator.Unit";

            if (msg.HasRequiresTenant)
            {
                sb.AppendLine($"            // [RequiresTenant] on {msg.TypeFullName}");
                sb.AppendLine($"            TurboMediator.Enterprise.TurboMediatorBuilderExtensions.WithMultiTenancy<{msg.TypeFullName}, {responseType}>(builder);");
            }

            if (msg.HasAuthorize)
            {
                sb.AppendLine($"            // [Authorize] on {msg.TypeFullName}");
                sb.AppendLine($"            TurboMediator.Enterprise.TurboMediatorBuilderExtensions.WithAuthorization<{msg.TypeFullName}, {responseType}>(builder);");
            }

            if (msg.HasTransactional)
            {
                sb.AppendLine($"            // [Transactional] on {msg.TypeFullName}");
                sb.AppendLine($"            TurboMediator.Persistence.TurboMediatorBuilderExtensions.WithTransaction<{msg.TypeFullName}, {responseType}>(builder);");
            }

            if (msg.HasAuditable)
            {
                sb.AppendLine($"            // [Auditable] on {msg.TypeFullName}");
                sb.AppendLine($"            TurboMediator.Persistence.TurboMediatorBuilderExtensions.WithAudit<{msg.TypeFullName}, {responseType}>(builder);");
            }

            if (msg.HasWithOutbox)
            {
                sb.AppendLine($"            // [WithOutbox] on {msg.TypeFullName}");
                sb.AppendLine($"            builder.Services.AddScoped<INotificationHandler<{msg.TypeFullName}>, TurboMediator.Persistence.Outbox.OutboxBehavior<{msg.TypeFullName}>>();");
            }
        }

        sb.AppendLine("            return builder;");
        sb.AppendLine("        }");
    }

    private static void GenerateConfigureRecurringJobs(StringBuilder sb, ImmutableArray<HandlerInfo> handlers)
    {
        var recurringHandlers = handlers
            .Where(h => !h.IsAbstract && h.RecurringJobId != null && h.RecurringJobCronExpression != null)
            .ToList();

        if (recurringHandlers.Count == 0) return;

        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Auto-registers recurring jobs discovered from [RecurringJob] attributes at compile time.");
        sb.AppendLine("        /// Zero reflection — all dispatch entries are generated as typed lambdas.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        /// <remarks>");
        sb.AppendLine("        /// Usage: scheduling.ConfigureRecurringJobsFromAttributes();");
        sb.AppendLine("        /// </remarks>");
        sb.AppendLine("        public static TurboMediator.Scheduling.DependencyInjection.SchedulingBuilder ConfigureRecurringJobsFromAttributes(");
        sb.AppendLine("            this TurboMediator.Scheduling.DependencyInjection.SchedulingBuilder builder)");
        sb.AppendLine("        {");

        foreach (var handler in recurringHandlers)
        {
            var commandType = handler.MessageTypeFullName;
            var responseType = handler.ResponseTypeFullName ?? "TurboMediator.Unit";
            var jobId = handler.RecurringJobId;
            var cron = handler.RecurringJobCronExpression;

            sb.AppendLine($"            // [RecurringJob(\"{jobId}\", \"{cron}\")] on {handler.HandlerTypeFullName}");

            if (handler.ResponseTypeFullName != null && handler.ResponseTypeFullName != "TurboMediator.Unit")
            {
                sb.AppendLine($"            builder.AddRecurringJob<{commandType}, {responseType}>(\"{jobId}\")");
            }
            else
            {
                sb.AppendLine($"            builder.AddRecurringJob<{commandType}>(\"{jobId}\")");
            }

            sb.AppendLine($"                .WithCron(\"{cron}\")");
            sb.AppendLine($"                .WithData(() => new {commandType}());");
            sb.AppendLine();
        }

        sb.AppendLine("            return builder;");
        sb.AppendLine("        }");
    }

    private static string GetHandlerInterface(HandlerInfo handler)
    {
        var hasResponse = handler.ResponseTypeFullName != null;

        return (handler.Kind, handler.IsStreaming, hasResponse) switch
        {
            (HandlerKind.Request, false, true) => $"IRequestHandler<{handler.MessageTypeFullName}, {handler.ResponseTypeFullName}>",
            (HandlerKind.Request, false, false) => $"IRequestHandler<{handler.MessageTypeFullName}>",
            (HandlerKind.Command, false, true) => $"ICommandHandler<{handler.MessageTypeFullName}, {handler.ResponseTypeFullName}>",
            (HandlerKind.Command, false, false) => $"ICommandHandler<{handler.MessageTypeFullName}>",
            (HandlerKind.Query, false, _) => $"IQueryHandler<{handler.MessageTypeFullName}, {handler.ResponseTypeFullName}>",
            (HandlerKind.Notification, false, _) => $"INotificationHandler<{handler.MessageTypeFullName}>",
            (HandlerKind.Request, true, _) => $"IStreamRequestHandler<{handler.MessageTypeFullName}, {handler.ResponseTypeFullName}>",
            (HandlerKind.Command, true, _) => $"IStreamCommandHandler<{handler.MessageTypeFullName}, {handler.ResponseTypeFullName}>",
            (HandlerKind.Query, true, _) => $"IStreamQueryHandler<{handler.MessageTypeFullName}, {handler.ResponseTypeFullName}>",
            _ => throw new InvalidOperationException($"Unknown handler kind: {handler.Kind}")
        };
    }
