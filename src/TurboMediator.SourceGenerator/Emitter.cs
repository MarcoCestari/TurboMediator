        sb.AppendLine("            var exceptionHandlerInterfaces = handlerType.GetInterfaces()");
        sb.AppendLine("                .Where(i => i.IsGenericType && i.GetGenericTypeDefinition() == typeof(IMessageExceptionHandler<,,>));");
