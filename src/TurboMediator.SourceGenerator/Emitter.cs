    private static void GenerateStreamPipelineHelper(StringBuilder sb)
    {
        sb.AppendLine("        private async IAsyncEnumerable<TTarget> ExecuteStreamWithPipeline<TMessage, TResponse, TTarget>(");
        sb.AppendLine("            TMessage message,");
        sb.AppendLine("            StreamHandlerDelegate<TResponse> handler,");
        sb.AppendLine("            [System.Runtime.CompilerServices.EnumeratorCancellation] CancellationToken cancellationToken)");
        sb.AppendLine("            where TMessage : IStreamMessage");
        sb.AppendLine("        {");
        sb.AppendLine("            // Get pre-processors");
        sb.AppendLine("            var preProcessors = _serviceProvider.GetServices<IStreamPreProcessor<TMessage>>();");
        sb.AppendLine("            foreach (var preProcessor in preProcessors)");
        sb.AppendLine("            {");
        sb.AppendLine("                await preProcessor.Process(message, cancellationToken);");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            // Get pipeline behaviors");
        sb.AppendLine("            var behaviors = _serviceProvider.GetServices<IStreamPipelineBehavior<TMessage, TResponse>>().ToArray();");
        sb.AppendLine();
        sb.AppendLine("            // Build the pipeline");
        sb.AppendLine("            StreamHandlerDelegate<TResponse> next = handler;");
        sb.AppendLine();
        sb.AppendLine("            for (int i = behaviors.Length - 1; i >= 0; i--)");
        sb.AppendLine("            {");
        sb.AppendLine("                var behavior = behaviors[i];");
        sb.AppendLine("                var currentNext = next;");
        sb.AppendLine("                next = () => behavior.Handle(message, currentNext, cancellationToken);");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            // Execute and get stream");
        sb.AppendLine("            var stream = next();");
        sb.AppendLine();
        sb.AppendLine("            // Get post-processors and wrap stream if any");
        sb.AppendLine("            var postProcessors = _serviceProvider.GetServices<IStreamPostProcessor<TMessage, TResponse>>().ToArray();");
        sb.AppendLine("            foreach (var postProcessor in postProcessors)");
        sb.AppendLine("            {");
        sb.AppendLine("                stream = postProcessor.Process(message, stream, cancellationToken);");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            // Yield items");
        sb.AppendLine("            await foreach (var item in stream.WithCancellation(cancellationToken))");
        sb.AppendLine("            {");
        sb.AppendLine("                yield return (TTarget)(object)item!;");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();
    }

